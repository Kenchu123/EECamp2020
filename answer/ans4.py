# Example 4:
# 
# Print the sum of the numers from 1 to 100.
# 
##########################################################################################

# while loop:
sum, i = 0, 0
while i <= 100:
    sum += i
    i += 1

# for loop:
sum = 0
for i in range(100): 
    sum += (i + 1)

print(f'sum = {sum}')

